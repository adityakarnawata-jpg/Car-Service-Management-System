CREATE DATABASE Car_ServiceDB;
USE Car_ServiceDB;

CREATE TABLE Customer (
    C_id    INT IDENTITY(1,1) PRIMARY KEY,
    C_name  VARCHAR(100) NOT NULL,
    Phone   VARCHAR(15)  NOT NULL UNIQUE,
    Email   VARCHAR(100) UNIQUE,
    Address VARCHAR(255)
);

CREATE TABLE Car (
    Car_id              INT IDENTITY(1,1) PRIMARY KEY,
    Registration_number VARCHAR(20) NOT NULL UNIQUE,
    Brand               VARCHAR(50) NOT NULL,
    Model               VARCHAR(50) NOT NULL,
    Manu_Year           INT         NOT NULL,
    C_id                INT         NOT NULL,
    FOREIGN KEY (C_id) REFERENCES Customer(C_id) ON DELETE CASCADE
);

CREATE TABLE Mechanic (
    M_id           INT IDENTITY(1,1) PRIMARY KEY,
    MName          VARCHAR(100) NOT NULL,
    Phone          VARCHAR(15)  NOT NULL UNIQUE,
    Specialization VARCHAR(100)
);

CREATE TABLE Spare_Part (
    Part_id INT IDENTITY(1,1) PRIMARY KEY,
    PName   VARCHAR(100)  NOT NULL,
    Price   DECIMAL(10,2) NOT NULL,
    Stock   INT           NOT NULL CHECK (Stock >= 0)
);

CREATE TABLE Service_Request (
    Service_id     INT IDENTITY(1,1) PRIMARY KEY,
    Service_date   DATE        NOT NULL,
    Problem        VARCHAR(MAX),
    Service_status VARCHAR(50) NOT NULL DEFAULT 'Pending',
    Car_id         INT         NOT NULL,
    M_id           INT,
    FOREIGN KEY (Car_id) REFERENCES Car(Car_id) ON DELETE CASCADE,
    FOREIGN KEY (M_id)   REFERENCES Mechanic(M_id) ON DELETE SET NULL
);

CREATE TABLE Uses (
    Service_id INT NOT NULL,
    Part_id    INT NOT NULL,
    Quantity   INT NOT NULL DEFAULT 1 CHECK (Quantity > 0),
    PRIMARY KEY (Service_id, Part_id),
    FOREIGN KEY (Service_id) REFERENCES Service_Request(Service_id) ON DELETE CASCADE,
    FOREIGN KEY (Part_id)    REFERENCES Spare_Part(Part_id)
);

CREATE TABLE Bill (
    Bill_id      INT IDENTITY(1,1) PRIMARY KEY,
    Bill_date    DATE          NOT NULL,
    Total_Amount DECIMAL(10,2) NOT NULL,
    Payment_Mode VARCHAR(50)   NOT NULL,
    Service_id   INT           NOT NULL UNIQUE,
    FOREIGN KEY (Service_id) REFERENCES Service_Request(Service_id) ON DELETE CASCADE
);

